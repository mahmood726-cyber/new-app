/**
 * R Code Generator for Meta-Analysis
 *
 * Generates reproducible R code using the metafor package for all analyses
 * performed in the platform. Enables users to verify results and extend
 * analyses in R.
 *
 * Reference: Viechtbauer W. Conducting Meta-Analyses in R with the metafor Package.
 * J Stat Softw. 2010;36(3):1-48.
 */

/**
 * Main R Code Generator Class
 */
export class RCodeGenerator {
    constructor(options = {}) {
        this.options = {
            includeComments: options.includeComments ?? true,
            includeDataTable: options.includeDataTable ?? true,
            formatOutput: options.formatOutput ?? true,
            packageVersion: options.packageVersion || '4.0-0'
        };
    }

    /**
     * Generate complete R script for meta-analysis
     * @param {Object} analysis - Analysis configuration and results
     * @param {Array} studies - Study data
     * @returns {string} Complete R script
     */
    generateFullScript(analysis, studies) {
        const sections = [];

        // Header
        sections.push(this.generateHeader(analysis));

        // Package loading
        sections.push(this.generatePackageLoading());

        // Data
        sections.push(this.generateDataSection(studies, analysis.effectMeasure));

        // Main meta-analysis
        sections.push(this.generateMetaAnalysis(analysis));

        // Forest plot
        sections.push(this.generateForestPlot(analysis));

        // Heterogeneity assessment
        sections.push(this.generateHeterogeneitySection(analysis));

        // Publication bias
        if (analysis.includePubBias !== false) {
            sections.push(this.generatePublicationBiasSection(analysis));
        }

        // Sensitivity analyses
        if (analysis.includeSensitivity !== false) {
            sections.push(this.generateSensitivitySection(analysis));
        }

        // Export section
        sections.push(this.generateExportSection(analysis));

        return sections.join('\n\n');
    }

    /**
     * Generate script header with metadata
     */
    generateHeader(analysis) {
        const date = new Date().toISOString().split('T')[0];
        const title = analysis.title || 'Meta-Analysis';

        let header = `#===============================================================================
# ${title}
# Generated by Meta-Analysis Platform v2.0
# Date: ${date}
#
# This script reproduces the meta-analysis using the metafor package in R.
# Reference: Viechtbauer W. J Stat Softw. 2010;36(3):1-48.
#===============================================================================
`;
        return header;
    }

    /**
     * Generate package loading section
     */
    generatePackageLoading() {
        return `#-------------------------------------------------------------------------------
# Load Required Packages
#-------------------------------------------------------------------------------
# Install packages if not already installed
if (!require("metafor")) install.packages("metafor")
if (!require("meta")) install.packages("meta")

library(metafor)
library(meta)

# Set random seed for reproducibility
set.seed(12345)
`;
    }

    /**
     * Generate data section with study data
     */
    generateDataSection(studies, effectMeasure = 'SMD') {
        if (!studies || studies.length === 0) {
            return '# No study data provided\ndat <- data.frame()';
        }

        let code = `#-------------------------------------------------------------------------------
# Study Data
#-------------------------------------------------------------------------------
`;

        // Determine data structure based on available fields
        const hasRawData = studies.some(s => s.n1 && s.n2);
        const hasPrecomputed = studies.some(s => s.yi !== undefined && s.vi !== undefined);

        if (hasPrecomputed) {
            // Pre-computed effect sizes
            code += `# Pre-computed effect sizes and variances
dat <- data.frame(
  study = c(${studies.map(s => `"${this.escapeR(s.name || s.study || 'Study')}"`).join(', ')}),
  yi = c(${studies.map(s => this.formatNumber(s.yi)).join(', ')}),
  vi = c(${studies.map(s => this.formatNumber(s.vi)).join(', ')}),
  sei = c(${studies.map(s => this.formatNumber(Math.sqrt(s.vi || 0))).join(', ')})
)
`;
        } else if (hasRawData) {
            // Raw data - need to calculate effect sizes
            const isBinary = studies.some(s => s.ai !== undefined || s.events1 !== undefined);

            if (isBinary) {
                // Binary outcome data
                code += `# Binary outcome data (2x2 table format)
dat <- data.frame(
  study = c(${studies.map(s => `"${this.escapeR(s.name || s.study || 'Study')}"`).join(', ')}),
  ai = c(${studies.map(s => s.ai || s.events1 || 0).join(', ')}),  # Events in treatment
  bi = c(${studies.map(s => s.bi || (s.n1 - (s.events1 || 0)) || 0).join(', ')}),  # Non-events in treatment
  ci = c(${studies.map(s => s.ci || s.events2 || 0).join(', ')}),  # Events in control
  di = c(${studies.map(s => s.di || (s.n2 - (s.events2 || 0)) || 0).join(', ')}),  # Non-events in control
  n1i = c(${studies.map(s => s.n1 || 0).join(', ')}),  # Total treatment
  n2i = c(${studies.map(s => s.n2 || 0).join(', ')})   # Total control
)

# Calculate effect sizes (${effectMeasure})
dat <- escalc(measure = "${effectMeasure}", ai = ai, bi = bi, ci = ci, di = di, data = dat)
`;
            } else {
                // Continuous outcome data
                code += `# Continuous outcome data
dat <- data.frame(
  study = c(${studies.map(s => `"${this.escapeR(s.name || s.study || 'Study')}"`).join(', ')}),
  m1i = c(${studies.map(s => this.formatNumber(s.m1 || s.mean1)).join(', ')}),  # Mean treatment
  sd1i = c(${studies.map(s => this.formatNumber(s.sd1)).join(', ')}),  # SD treatment
  n1i = c(${studies.map(s => s.n1 || 0).join(', ')}),  # N treatment
  m2i = c(${studies.map(s => this.formatNumber(s.m2 || s.mean2)).join(', ')}),  # Mean control
  sd2i = c(${studies.map(s => this.formatNumber(s.sd2)).join(', ')}),  # SD control
  n2i = c(${studies.map(s => s.n2 || 0).join(', ')})   # N control
)

# Calculate effect sizes (${effectMeasure})
dat <- escalc(measure = "${effectMeasure}", m1i = m1i, sd1i = sd1i, n1i = n1i,
              m2i = m2i, sd2i = sd2i, n2i = n2i, data = dat)
`;
            }
        }

        code += `
# View data
print(dat)
summary(dat$yi)
`;

        return code;
    }

    /**
     * Generate main meta-analysis section
     */
    generateMetaAnalysis(analysis) {
        const method = this.mapEstimatorToR(analysis.method || 'REML');
        const hksj = analysis.hksj !== false;

        let code = `#-------------------------------------------------------------------------------
# Random-Effects Meta-Analysis
#-------------------------------------------------------------------------------
`;

        // Main analysis
        code += `# Fit random-effects model using ${method} estimator
res <- rma(yi, vi, data = dat, method = "${method}"${hksj ? ', test = "knha"' : ''})

# Summary of results
summary(res)

# Confidence interval for tau^2
confint(res)
`;

        // Add prediction interval
        code += `
# Prediction interval (expected range of true effects in new studies)
predict(res, transf = ${this.getTransform(analysis.effectMeasure)})
`;

        // Profile likelihood if REML
        if (method === 'REML' || method === 'ML') {
            code += `
# Profile likelihood confidence interval for tau^2
profile(res, xlim = c(0, max(0.5, res$tau2 * 2)))
`;
        }

        return code;
    }

    /**
     * Generate forest plot section
     */
    generateForestPlot(analysis) {
        const measure = analysis.effectMeasure || 'SMD';
        const atransf = this.getBackTransform(measure);

        return `#-------------------------------------------------------------------------------
# Forest Plot
#-------------------------------------------------------------------------------
# Basic forest plot
forest(res,
       header = TRUE,
       slab = dat$study,
       xlab = "${this.getEffectLabel(measure)}",
       ${atransf ? `atransf = ${atransf},` : ''}
       order = "obs",
       showweights = TRUE,
       col = "darkblue",
       border = "darkblue")

# Add prediction interval to plot
addpoly(res, row = -1, mlab = "Prediction interval",
        annotate = TRUE, ${atransf ? `atransf = ${atransf},` : ''}
        col = "darkred")

# Enhanced forest plot with subgroups (if applicable)
# forest(res, xlim = c(-16, 8), at = log(c(0.25, 0.5, 1, 2, 4)),
#        ilab = cbind(dat$n1i, dat$n2i),
#        ilab.xpos = c(-10, -8), cex = 0.75)
`;
    }

    /**
     * Generate heterogeneity assessment section
     */
    generateHeterogeneitySection(analysis) {
        return `#-------------------------------------------------------------------------------
# Heterogeneity Assessment
#-------------------------------------------------------------------------------
# Heterogeneity statistics from model
cat("\\n=== Heterogeneity Statistics ===\\n")
cat(sprintf("Q = %.2f, df = %d, p = %.4f\\n", res$QE, res$k - 1, res$QEp))
cat(sprintf("tau^2 = %.4f (SE = %.4f)\\n", res$tau2, res$se.tau2))
cat(sprintf("tau = %.4f\\n", sqrt(res$tau2)))
cat(sprintf("I^2 = %.1f%%\\n", res$I2))
cat(sprintf("H^2 = %.2f\\n", res$H2))

# Q-profile confidence intervals for I^2 and tau^2
qprofile_ci <- confint(res, type = "QP")
print(qprofile_ci)

# Compare different tau^2 estimators
cat("\\n=== Sensitivity to tau^2 Estimator ===\\n")
estimators <- c("DL", "REML", "PM", "SJ", "HE", "HS", "ML")
tau2_comparison <- sapply(estimators, function(m) {
  tryCatch({
    fit <- rma(yi, vi, data = dat, method = m)
    c(tau2 = fit$tau2, I2 = fit$I2, effect = fit$beta)
  }, error = function(e) c(tau2 = NA, I2 = NA, effect = NA))
})
print(t(tau2_comparison))
`;
    }

    /**
     * Generate publication bias section
     */
    generatePublicationBiasSection(analysis) {
        return `#-------------------------------------------------------------------------------
# Publication Bias Assessment
#-------------------------------------------------------------------------------
# Funnel plot
funnel(res, xlab = "Effect Size", ylab = "Standard Error",
       level = c(90, 95, 99), shade = c("white", "gray75", "gray55"),
       legend = TRUE)

# Contour-enhanced funnel plot
funnel(res, level = c(0.90, 0.95, 0.99), shade = c("white", "gray75", "gray55"),
       refline = 0, legend = TRUE, back = "gray90",
       xlab = "Effect Size", ylab = "Standard Error")

# Egger's regression test for funnel plot asymmetry
cat("\\n=== Egger's Test ===\\n")
regtest(res, model = "lm")

# Begg's rank correlation test
cat("\\n=== Begg's Rank Correlation Test ===\\n")
ranktest(res)

# Trim and fill analysis
cat("\\n=== Trim and Fill ===\\n")
tf <- trimfill(res)
print(tf)
funnel(tf, legend = TRUE)

# PET-PEESE regression-based corrections
cat("\\n=== PET-PEESE Analysis ===\\n")
# PET: Regress on SE
pet <- rma(yi, vi, mods = ~ sei, data = dat)
cat("PET (intercept = bias-corrected effect):\\n")
print(coef(summary(pet)))

# PEESE: Regress on variance
peese <- rma(yi, vi, mods = ~ vi, data = dat)
cat("\\nPEESE (intercept = bias-corrected effect):\\n")
print(coef(summary(peese)))

# Selection model (if package available)
if (require("weightr")) {
  cat("\\n=== Vevea-Hedges Selection Model ===\\n")
  sel <- weightfunct(dat$yi, dat$vi, steps = c(0.025, 1))
  print(sel)
}

# p-curve analysis (if package available)
if (require("dmetar")) {
  cat("\\n=== P-Curve Analysis ===\\n")
  pcurve(res)
}
`;
    }

    /**
     * Generate sensitivity analysis section
     */
    generateSensitivitySection(analysis) {
        return `#-------------------------------------------------------------------------------
# Sensitivity Analyses
#-------------------------------------------------------------------------------
# Leave-one-out analysis
cat("\\n=== Leave-One-Out Analysis ===\\n")
loo <- leave1out(res)
print(loo)

# Forest plot of leave-one-out
forest(loo$estimate, sei = loo$se, slab = dat$study,
       xlab = "Effect Size (omitting each study)",
       header = "Leave-One-Out Analysis")

# Influential case diagnostics
cat("\\n=== Influence Diagnostics ===\\n")
inf <- influence(res)
print(inf)
plot(inf)

# GOSH plot for detecting heterogeneity sources
# Note: Computationally intensive for large numbers of studies
if (nrow(dat) <= 15) {
  cat("\\n=== GOSH Plot ===\\n")
  gosh_res <- gosh(res, subsets = 10000)
  plot(gosh_res, out = inf, breaks = 50)
}

# Cumulative meta-analysis (by year or precision)
cat("\\n=== Cumulative Meta-Analysis ===\\n")
cum <- cumul(res, order = 1:nrow(dat))
forest(cum, xlab = "Cumulative Effect Size")

# Baujat plot (contribution to heterogeneity vs influence)
cat("\\n=== Baujat Plot ===\\n")
baujat(res)
`;
    }

    /**
     * Generate export section
     */
    generateExportSection(analysis) {
        return `#-------------------------------------------------------------------------------
# Export Results
#-------------------------------------------------------------------------------
# Create summary table
results_table <- data.frame(
  Statistic = c("k (studies)", "Pooled Effect", "SE", "95% CI Lower", "95% CI Upper",
                "z/t-value", "p-value", "tau^2", "tau", "I^2 (%)", "Q", "Q p-value"),
  Value = c(
    res$k,
    round(res$beta, 4),
    round(res$se, 4),
    round(res$ci.lb, 4),
    round(res$ci.ub, 4),
    round(res$zval, 4),
    formatC(res$pval, format = "e", digits = 3),
    round(res$tau2, 4),
    round(sqrt(res$tau2), 4),
    round(res$I2, 1),
    round(res$QE, 2),
    formatC(res$QEp, format = "e", digits = 3)
  )
)

print(results_table)

# Export to CSV
# write.csv(results_table, "meta_analysis_results.csv", row.names = FALSE)
# write.csv(dat, "study_data.csv", row.names = FALSE)

# Session info for reproducibility
cat("\\n=== Session Information ===\\n")
sessionInfo()
`;
    }

    /**
     * Map JavaScript estimator name to R metafor name
     */
    mapEstimatorToR(method) {
        const map = {
            'REML': 'REML',
            'DL': 'DL',
            'PM': 'PM',
            'SJ': 'SJ',
            'HE': 'HE',
            'HS': 'HS',
            'ML': 'ML',
            'EB': 'EB',
            'FE': 'FE'
        };
        return map[method] || 'REML';
    }

    /**
     * Get transformation function for effect measure
     */
    getTransform(measure) {
        const transforms = {
            'OR': 'exp',
            'RR': 'exp',
            'HR': 'exp',
            'SMD': 'NULL',
            'MD': 'NULL',
            'COR': 'transf.ztor',
            'ZCOR': 'transf.ztor'
        };
        return transforms[measure] || 'NULL';
    }

    /**
     * Get back-transformation for forest plots
     */
    getBackTransform(measure) {
        const transforms = {
            'OR': 'exp',
            'RR': 'exp',
            'HR': 'exp',
            'SMD': null,
            'MD': null,
            'COR': 'transf.ztor'
        };
        return transforms[measure] || null;
    }

    /**
     * Get effect size label
     */
    getEffectLabel(measure) {
        const labels = {
            'OR': 'Odds Ratio',
            'RR': 'Risk Ratio',
            'HR': 'Hazard Ratio',
            'SMD': 'Standardized Mean Difference',
            'MD': 'Mean Difference',
            'COR': 'Correlation',
            'ZCOR': "Fisher's z"
        };
        return labels[measure] || 'Effect Size';
    }

    /**
     * Format number for R
     */
    formatNumber(n) {
        if (n === null || n === undefined || isNaN(n)) return 'NA';
        return Number(n).toFixed(6);
    }

    /**
     * Escape string for R
     */
    escapeR(str) {
        if (!str) return '';
        return String(str).replace(/\\/g, '\\\\').replace(/"/g, '\\"');
    }

    /**
     * Generate R code for specific analysis type
     */
    generateSubgroupAnalysis(analysis, subgroupVar) {
        return `#-------------------------------------------------------------------------------
# Subgroup Analysis by ${subgroupVar}
#-------------------------------------------------------------------------------
# Mixed-effects model with subgroup as moderator
res_subgroup <- rma(yi, vi, mods = ~ ${subgroupVar}, data = dat)
summary(res_subgroup)

# Separate meta-analysis for each subgroup
subgroups <- unique(dat$${subgroupVar})
for (sg in subgroups) {
  cat(sprintf("\\n=== Subgroup: %s ===\\n", sg))
  dat_sg <- subset(dat, ${subgroupVar} == sg)
  if (nrow(dat_sg) >= 2) {
    res_sg <- rma(yi, vi, data = dat_sg, method = "REML")
    print(summary(res_sg))
  }
}

# Test for subgroup differences
cat("\\n=== Test for Subgroup Differences ===\\n")
print(anova(res_subgroup))
`;
    }

    /**
     * Generate meta-regression code
     */
    generateMetaRegression(analysis, moderators) {
        const modFormula = moderators.map(m => m.name).join(' + ');

        return `#-------------------------------------------------------------------------------
# Meta-Regression
#-------------------------------------------------------------------------------
# Model with moderators: ${modFormula}
res_reg <- rma(yi, vi, mods = ~ ${modFormula}, data = dat)
summary(res_reg)

# Variance explained by moderators (R^2)
cat(sprintf("\\nR^2 = %.1f%%\\n", res_reg$R2))

# Bubble plot for single continuous moderator
# (uncomment for single moderator)
# regplot(res_reg, xlab = "${moderators[0]?.name || 'Moderator'}",
#         ylab = "Effect Size", transf = exp)

# Permutation test for moderator significance
# permutest(res_reg, iter = 1000)
`;
    }

    /**
     * Generate network meta-analysis code
     */
    generateNetworkMA(analysis, treatments) {
        return `#-------------------------------------------------------------------------------
# Network Meta-Analysis
#-------------------------------------------------------------------------------
# Note: Requires netmeta package
if (!require("netmeta")) install.packages("netmeta")
library(netmeta)

# Prepare data for network meta-analysis
# Assumes data has: study, treat1, treat2, effect, se
nma <- netmeta(TE = effect, seTE = se,
               treat1 = treat1, treat2 = treat2,
               studlab = study, data = dat,
               sm = "${analysis.effectMeasure || 'SMD'}",
               reference.group = "${treatments[0] || 'Control'}")

summary(nma)

# Network graph
netgraph(nma, plastic = FALSE, points = TRUE,
         cex.points = 3, col = "darkblue")

# Forest plot of NMA results
forest(nma, reference.group = "${treatments[0] || 'Control'}")

# League table
netleague(nma, digits = 2)

# Ranking (P-scores)
netrank(nma)
`;
    }
}

/**
 * Generate quick R code snippet for specific analysis
 */
export function generateQuickRCode(analysis, studies) {
    const generator = new RCodeGenerator({ includeComments: false });
    return generator.generateFullScript(analysis, studies);
}

/**
 * Export R code to file
 */
export function exportRScript(analysis, studies, filename = 'meta_analysis.R') {
    const generator = new RCodeGenerator();
    const code = generator.generateFullScript(analysis, studies);

    const blob = new Blob([code], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = filename;
    a.click();
    URL.revokeObjectURL(url);

    return code;
}

export default RCodeGenerator;
